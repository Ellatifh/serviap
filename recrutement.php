<script>
    $(document).ready(function(){
        
    })
    $("form").on('submit',function (event) {
          event.preventDefault();
          //grab all form data  
          //var formData = $(this).serialize();
    
          $.ajax({
              url: 'backend.php',
              type: 'POST',
              data: new FormData(this),
              async: false,
              contentType: false,
              cache: false,
              processData:false,
              dataType:"json",
              beforeSend:function(){
                $('#div_btn').html('<button type="submit" class="btn btn-template-outlined btn-send" id="btn-send"><i class="fa fa-refresh fa-spin"></i> Envoyer</button>');  
              },
              success: function (data) {
                 console.log(data);
                  if(data.status=="passed"){
                    $('#msg').html('<div class="alert alert-success" role="alert">Votre message a été envoyé avec success</div>');
                    $('#firstname').val('');
                    $('#lastname').val('');
                    $('#telephone').val('');
                    $('#email').val('');
                    $('#message').val('');
                   }else{
                      $('#msg').html('<div class="alert alert-danger" role="alert">'+data.message+'</div>');
                   }
                   $('#div_btn').html('<button type="submit" class="btn btn-template-outlined btn-send" id="btn-send"><i class="fa fa-send"></i> Envoyer</button>');  
              },
              error: function () {
                  alert("error in ajax form submission");
              }
          });
    
          return false;
      });
</script>